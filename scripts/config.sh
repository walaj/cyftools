function check_file_exists {
    local file="$1"
    if [[ ! -f "$file" ]]; then
        echo "File not found: $file"
        exit 1
    fi
}

function contains_string {
    local list="$1"
    local string_to_check="$2"
    local match_found=1

    IFS=',' read -ra ADDR <<< "$list"
    for i in "${ADDR[@]}"; do
        if [[ $string_to_check == *"$i"* ]]; then
            match_found=0
            break
        fi
    done

    return $match_found
}

